--- Generate Unicode block ASCII art for the given text
---@param text string The text to convert to block art
---@return string The generated ASCII art as a multi-line string
function M.generate(text)
	text = text:upper()

	-- Get the number of rows (all characters should have same height)
	local num_rows = #FONT.A

	-- Build each row
	local lines = {}
	for row = 1, num_rows do
		local line = ""
		for i = 1, #text do
			local char = text:sub(i, i)
			if FONT[char] then
				line = line .. FONT[char][row]
			else
				-- Replace unsupported characters with space
				line = line .. FONT[" "][row]
			end
		end
		table.insert(lines, line)
	end

	return table.concat(lines, "\n")
end

--- Generate block text and return as a table of lines (useful for dashboard configs)
---@param text string The text to convert to block art
---@return table Lines of ASCII art
function M.generate_lines(text)
	local result = M.generate(text)
	local lines = {}
	for line in result:gmatch("[^\n]+") do
		table.insert(lines, line)
	end
	return lines
end

function M.get_header()
	local host = vim.fn.hostname()

	if host == "FYYW0R9QG9" then
		return M.generate("DCL-Navigator")
	else
		return M.generate("jhamill")
	end
end

